void vga_set_videomode(struct wrect_driver_info *drv, int mode, int mapped)
{
  char *v;
  int i;

  switch(mode) {
  case VGA_MODE_TEXT80x25:
    vga_write_regs(g_80x25_text);
    vga_write_font(g_8x16_font, 16);
    v=(char*)CFG_MEM_VGATEXT;
    for(i = 0; i < 80 * 25; i++) {
      *v++ =0;
      *v++ =7;
    }
    break;
  case VGA_MODE_GRAPH640x480x16:
    drv->width=640;
    drv->height=480;
    drv->videobuff=(unsigned char*)CFG_MEM_VGAGRAPHICS;
    drv->mapped=1;
    drv->colorcode_white=15;
    drv->colorcode_black=0;
    drv->colorcode_gray=7;
    drv->color=-1;
    drv->fn=&g_640x480x16_func;
    g_640x480x16_func.draw_pixel=vga_draw_pixel4p;
    g_640x480x16_func.draw_horzline=vga_draw_horzline4p;
    g_640x480x16_func.draw_vertline=vga_draw_vertline4p;
    g_640x480x16_func.draw_bitmap=vga_draw_bitmap4p;
    g_640x480x16_func.draw_pixmap=vga_draw_pixmap4p;
    g_640x480x16_func.draw_char=vga_draw_char;
    g_640x480x16_func.set_colorcode=vga_set_colorcode4p;
    g_640x480x16_func.set_textbgcolor=vga_set_textbgcolor;
    g_640x480x16_func.set_calcmode=vga_set_calcmode4p;

    vga_write_regs(g_640x480x16);

    vga_set_accessmode4p(3);
    vga_set_colormask4p(0xf);
    vga_set_mask4p(0xff);

    drv->fn->set_textbgcolor(drv,drv->colorcode_black);
    drv->fn->set_calcmode(drv,WRECT_GC_MODE_PSET);
    vga_init_pallet(g_pallet);
    vga_write_pallet(0,15,g_pallet);
    break;

  case VGA_MODE_GRAPH320x200x256:
    drv->width=320;
    drv->height=200;
    drv->videobuff=(unsigned char*)CFG_MEM_VGAGRAPHICS;
    drv->mapped=mapped;
    drv->colorcode_white=63;
    drv->colorcode_black=0;
    drv->colorcode_gray=7;
    drv->fn=&g_320x200x256_func;
    g_320x200x256_func.draw_pixel=vga_draw_pixel8;
    g_320x200x256_func.draw_horzline=vga_draw_horzline8;
    g_320x200x256_func.draw_vertline=vga_draw_vertline8;
    g_320x200x256_func.draw_bitmap=vga_draw_bitmap8;
    g_320x200x256_func.draw_pixmap=vga_draw_pixmap8;
    g_320x200x256_func.draw_char=vga_draw_char;
    g_320x200x256_func.set_colorcode=vga_set_colorcode8;
    g_320x200x256_func.set_textbgcolor=vga_set_textbgcolor;
    g_320x200x256_func.set_calcmode=vga_set_calcmode8;


    if(mapped) {
      vga_write_regs(g_320x200x256);
    }
    drv->fn->set_textbgcolor(drv,drv->colorcode_black);
    drv->fn->set_calcmode(drv,WRECT_GC_MODE_PSET);

    if(mapped) {
      vga_init_pallet(g_pallet);
      vga_write_pallet(0,255,g_pallet);
    }

    break;
  }
}


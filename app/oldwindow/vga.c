//static 
void vga_draw_bitmap4p(struct wrect_driver_info *drv, unsigned int x, unsigned int y, unsigned char *chr, int ysize, struct WRECT *clip)
{
  unsigned int wd_in_bytes,size;
  unsigned char *addr;
  unsigned char mask,mask0,mask1;
  unsigned char latch=0;

  size=ysize-clip->y1;
  if(size>clip->y2)
    size=clip->y2;
  chr=(unsigned char*)((unsigned int)chr+clip->y1);

  wd_in_bytes = drv->width / 8;
  addr = (unsigned char*)(wd_in_bytes * (y + clip->y1)+ ((x+clip->x1) >> 3) + CFG_MEM_VGAGRAPHICS);
  for(;size>0;size--) {
    mask=*chr & (0xff >> clip->x1) & (0xff << (8-clip->x2));
    mask0 = mask >> (x&7);
    if(mask0!=0) {
      latch |= addr[0];
      addr[0] = mask0;
    }
    mask1 = mask << ( 8 - (x&7));
    if(mask1!=0) {
      latch |= addr[1];
      addr[1] = mask1;
    }
    chr++;
    addr = (unsigned char*)((unsigned int)addr + wd_in_bytes);
  }
  vga_latch_dmy = latch;
}

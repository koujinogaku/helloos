static void vga_dump(unsigned char *regs, unsigned count)
{
	unsigned i;
	char s[4];

	i = 0;
//	printf("\t");
	display_puts(" ");
	for(; count != 0; count--)
	{
//		printf("0x%02X,", *regs);
		display_puts(" ");
		byte2hex(*regs,s);
		display_puts(s);

		i++;
		if(i >= 8)
		{
			i = 0;
//			printf("\n\t");
			display_puts("\n ");
		}
		else
//			printf(" ");
			display_puts(" ");
		regs++;
	}
//	printf("\n");
	display_puts("\n");
}

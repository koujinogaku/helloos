void vga_draw_vertline4p(struct wrect_driver_info *drv, unsigned int x, unsigned int y1, unsigned int y2)
{
  unsigned int wd_in_bytes;
  unsigned char mask;
  char *addr;
  unsigned int y;
  unsigned char latch=0;

  if(x > drv->width-1)
    x=drv->width-1;
  if(y1>y2)
    y2=y1;
  if(y1 > drv->height-1)
    y1=drv->height-1;
  if(y2> drv->height-1)
    y2= drv->height-1;

  wd_in_bytes = drv->width / 8;
  addr = (char*)(wd_in_bytes * y1 + (x >> 3) + CFG_MEM_VGAGRAPHICS);
  mask = 0x80 >> (x&7);

  for(y=y1;y<=y2;y++) {
    latch |= *addr;
    *addr = mask;
    addr = (char*)((int)addr + wd_in_bytes);
  }
  vga_latch_dmy = latch;
}

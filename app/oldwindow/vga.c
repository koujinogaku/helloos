void vga_draw_horzline4p(struct wrect_driver_info *drv, unsigned int x1, unsigned int x2, unsigned int y)
{
  unsigned int wd_in_bytes;
  unsigned char mask1, mask2;
  char *addr;
  unsigned int x;
  unsigned char latch=0;

  if(x1 > drv->width - 1)
    x1=drv->width -1;
  if(x2>drv->width -1)
    x2=drv->width -1;
  if(x1>x2)
    x2=x1;
  if(y>drv->height -1)
    y=drv->height -1;

  wd_in_bytes = drv->width / 8;
  addr = (char*)(wd_in_bytes * y + (x1 >> 3) + CFG_MEM_VGAGRAPHICS);
  mask1 = 0xff >> (x1&7);
  mask2 = 0xff << (7-(x2&7));
  if((x1&0xfffffff8) == (x2&0xfffffff8)) {
    mask1 &= mask2;
    latch |= *addr;
    *addr = mask1;
    return ;
  }
  else {
    latch |= *addr;
    *addr = mask1;
  }
  addr++;
  for(x=(x1&0xfffffff8)+8; x<(x2&0xfffffff8) ; x+=8) {
    latch |= *addr;
    *addr = 0xff;
    addr++;
  }
  latch |= *addr;
  *addr = mask2;

  vga_latch_dmy = latch;
}

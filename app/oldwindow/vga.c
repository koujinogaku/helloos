void vga_read_pallet( int start, int end, char *pallet )
{
	int i;

	cpu_lock();
	cpu_out8(VGA_DAC_READ_INDEX, start);
	for (i = start; i <= end; i++) {
		pallet[0]=cpu_in8(VGA_DAC_DATA) << 2;
		pallet[1]=cpu_in8(VGA_DAC_DATA) << 2;
		pallet[2]=cpu_in8(VGA_DAC_DATA) << 2;
		pallet += 3;
	}
	cpu_unlock();
}
/*****************************************************************************
*****************************************************************************/
void vga_write_pallet( int start, int end, char *pallet )
{
	int i;

	cpu_lock();
	cpu_out8(VGA_DAC_WRITE_INDEX, start);
	for (i = start; i <= end; i++) {
		cpu_out8(VGA_DAC_DATA, pallet[0] >> 2);
		cpu_out8(VGA_DAC_DATA, pallet[1] >> 2);
		cpu_out8(VGA_DAC_DATA, pallet[2] >> 2);
		pallet += 3;
	}
	cpu_unlock();
}

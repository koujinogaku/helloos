void vga_draw_pixel4p(struct wrect_driver_info *drv, unsigned int x, unsigned int y)
{
  unsigned int wd_in_bytes, mask;
  char *addr;
  unsigned char latch=0;

  wd_in_bytes = drv->width / 8;
  addr = (char*)(wd_in_bytes * y + (x >> 3) + CFG_MEM_VGAGRAPHICS);
  mask = 0x80 >>(x & 7);
  latch |= *addr;
  *addr = mask;
  vga_latch_dmy = latch;
}
